public class ExemploDecisaoRepeticao {
    public static void main(String[] args) {
        // If / else
        double d1 = 9.0;
        double d2 = 13.0;

        if (d1 < d2) {
            // se a condicao for verdadeira
            System.out.println("d1 < d2");
        } else {
            //se for falsa
            System.out.println("d1 >= d2");

        }

        // for

        for (int i = 0; i < 10; i++) {
            System.out.println("i: " + i);

        }

        // while 
        System.out.println("Exemplo de while");
        int i  = 1;
        while (i < 10) {
            System.out.println("i : " + i);
            i++;
        }

        
    }
}
